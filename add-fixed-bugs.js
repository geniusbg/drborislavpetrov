async function addFixedBugs() {
  console.log('üêõ Adding fixed bugs to tracker...\n');
  
  let successCount = 0;
  let errorCount = 0;
  
  for (const bug of fixedBugs) {
    try {
      console.log(`üìù Adding: ${bug.title}`);
      
      const postData = JSON.stringify(bug);
      
      const options = {
        hostname: 'localhost',
        port: 3000,
        path: '/api/admin/bugs',
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'x-admin-token': 'admin-token',
          'Content-Length': Buffer.byteLength(postData)
        }
      };
      
      const req = http.request(options, (res) => {
        let data = '';
        res.on('data', (chunk) => {
          data += chunk;
        });
        
        res.on('end', () => {
          if (res.statusCode === 200) {
            console.log(`‚úÖ Success: ${bug.title}`);
            successCount++;
          } else {
            console.log(`‚ùå Failed: ${bug.title} - ${data}`);
            errorCount++;
          }
        });
      });
      
      req.on('error', (e) => {
        console.error(`‚ùå Error: ${bug.title} - ${e.message}`);
        errorCount++;
      });
      
      req.write(postData);
      req.end();
      
      // Wait between requests
      await new Promise(resolve => setTimeout(resolve, 500));
      
    } catch (error) {
      console.error(`‚ùå Error adding bug: ${bug.title}`, error);
      errorCount++;
    }
  }
  
  console.log(`\nüìä Summary:`);
  console.log(`‚úÖ Successfully added: ${successCount} bugs`);
  console.log(`‚ùå Failed to add: ${errorCount} bugs`);
  console.log(`üìù Total processed: ${fixedBugs.length} bugs`);
}

